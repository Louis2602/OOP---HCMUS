#include "OOPMath.h"

int main()
{
    cout << Math::absOOP(-10) << endl;
    cout << Math::powOOP(2, 3) << endl;
    cout << Math::sqrtOOP(3) << endl;
    cout << Math::ceilOOP(2.7) << endl;
    cout << Math::floorOOP(2.3) << endl;
    cout << Math::roundOOP(2.4) << endl;
    cout << Math::logOOP(5.5) << endl;
    cout << Math::log2OOP(3) << endl;
    cout << Math::sinOOP(90) << endl;
    cout << Math::cosOOP(60) << endl;
    cout << Math::tanOOP(30) << endl;
    cout << Math::asinOOP(0.5) << endl;
    cout << Math::acosOOP(0) << endl;
    cout << Math::atanOOP(1.73) << endl;
    cout << Math::expOOP(2.3) << endl;
    return 0;
}